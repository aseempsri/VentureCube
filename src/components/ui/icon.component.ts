  ngAfterViewInit() {
    this.updateIcon();
  }

  ngOnChanges(changes: SimpleChanges) {
    if (changes['name']) {
      // Wait for view to be ready
      if (this.svgElement) {
        this.updateIcon();
      } else {
        setTimeout(() => this.updateIcon(), 0);
      }
    }
  }

  private updateIcon() {
    const svgElement = this.svgElement?.nativeElement || this.elementRef.nativeElement.querySelector('svg');
    if (!svgElement || !this.name) {
      return;
    }

    const iconPath = this.icons[this.name];
    if (!iconPath) {
      console.warn(`Icon "${this.name}" not found. Available icons:`, Object.keys(this.icons));
      return;
    }

    // Clear existing children
    while (svgElement.firstChild) {
      this.renderer.removeChild(svgElement, svgElement.firstChild);
    }

    // Parse the SVG path string and create elements using Renderer2
    const parser = new DOMParser();
    const doc = parser.parseFromString(`<svg xmlns="http://www.w3.org/2000/svg">${iconPath}</svg>`, 'image/svg+xml');
    const paths = doc.documentElement.querySelectorAll('path');
    
    paths.forEach((path: Element) => {
      const newPath = this.renderer.createElement('path', 'http://www.w3.org/2000/svg');
      Array.from(path.attributes).forEach(attr => {
        this.renderer.setAttribute(newPath, attr.name, attr.value);
      });
      this.renderer.appendChild(svgElement, newPath);
    });
  }
